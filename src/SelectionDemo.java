public class SelectionDemo {
    public static void main(String [] args){
        int n=8;
        if(n%2==0)
            System.out.println("Even");

        if(n%2!=0)
            System.out.println("Odd");

        int m=0;
        if(m==0)
        { System.out.println("nothing");
            System.out.println("Bye");
        }
        else if(m%2==0) {
            System.out.println("Even");
        }
        else {
            System.out.println("Odd");
        }
    }
}
